#include <stdlib.h>
#include <stdio.h>
#include <string.h>

int					ft_strlen(char *str)
{
	unsigned int	len;

	len = 0;
	while (str[len] != '\0')
	{
		len++;
	}
	return (len);
}

char				*ft_strdup(char *src)
{
	char			*dest;
	unsigned int	i;
	unsigned int	len;

	len = ft_strlen(src) + 1;
	dest = (char *)malloc(sizeof(len));
	if (dest == NULL)
		return (NULL);
	i = 0;
	while (i < len)
	{
		dest[i] = src[i];
		i++;
	}
	return (dest);
}
